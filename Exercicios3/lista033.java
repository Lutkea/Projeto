package Exercicios3;

public class lista033{

    public static void main(String[] args) {
    
        int i = Integer.parseInt(args[0]);
        int j = Integer.parseInt(args[1]);
        int resultado;

        
      
        resultado = i + j;
        System.out.println(resultado);
        resultado = i - j;
        System.out.println(resultado);
        resultado = i * j;
        System.out.println(resultado);
        resultado = i / j;
        System.out.println(resultado);
        resultado = i % j;
        System.out.println(resultado);
    
        

    }
}